package entity;

public class Joiner {
	private String join_students;
	private String join_time;
	public Joiner()
	{
		
	}
	public String getJoin_students() {
		return join_students;
	}
	public void setJoin_students(String join_students) {
		this.join_students = join_students;
	}
	public String getJoin_time() {
		return join_time;
	}
	public void setJoin_time(String join_time) {
		this.join_time = join_time;
	}
	
	
}
